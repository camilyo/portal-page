    <p>Shopping carts are a program used to bridge the communication between an online retailer and its clients. According to bigcommerce.com, a shopping cart on an online retailer's site is a piece of software that facilitates the purchase of a product(s) or service(s). It accepts the customer's payment and organizes the distribution of that information to the merchant, payment processor and other parties. It is a very basic form where clients can buy from one to multiple items such as clothing, electronics and even food.</p>
    
    
    <p>Choosing which shopping cart software you will want to use in your website it is not an easy task. Ralph Steyers (2016) agrees that there are different things that will need to be consider first such as product descriptions and photography, inventory control, collecting and depositing taxes, setting up shipping systems and terms, and accepting customer payments. In addition, you'll need to provide customer service, make sure transactions are secure, and effectively market your products. It is important to understand the two different types of shopping cart: </p>
    
    
    <ol><li><b>Licensed Shopping Carts:</b> are a more powerful option available to online retailers, although they require more development time. They are hosted in the merchant’s own server where there is the possibility to change features if needed. By choosing the licensed shopping cart gives you the possibility of switch companies without hefty fees or risk of losing past data and records. The downside is that this option can be costly for a small company, since it is usually a one time fee and not a monthly fee.</li>
    
    <li><b>Hosted Shopping Carts:</b> Hosted companies can be a fast way to start selling online. Independent of the time it takes, it is usually much more time efficient than setting up a licensed one. The host companies are the ones responsible for server maintenance, backups and troubleshooting. Unfortunately, this solutio lack design freedom and your online store will have only the feature/solutions provided by the hosting company.</li></ol>
        
    <p>Don’t worry if you are still not confident about which one to choose, many companies offer a free trial and some of them offer both licensed and hosted shopping cart making it a hassle-free transition.</p>
        
